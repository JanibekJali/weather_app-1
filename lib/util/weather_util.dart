class WeatherUtil {
  static num relvinToCelsius(double kelvin) {
    return kelvin - 273.15.round();
  }

  static String getDescription(num temp) {
    if (temp > 25) {
      return 'It\'s 🍦 time';
    } else if (temp > 20) {
      return 'Time for shorts and 👕';
    } else if (temp < 10) {
      return 'You\'ll need 🧣 and 🧤';
    } else {
      return 'Bring a 🧥 just in case';
    }
  }
}
